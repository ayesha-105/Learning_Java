public class Display_array {
    public static void main(String[] args) {
        int [] marks = {1,2,3,4,5};
        //display array by using for loop
        System.out.println("Display array by using for loop");
        for(int i=0; i<marks.length; i++){
            System.out.println(marks[i]);
        }
    }
    
}
